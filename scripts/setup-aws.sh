#!/bin/bash

# AWS EKS Setup Script
set -e

# Colors for output
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
NC='\033[0m' # No Color

# Configuration
AWS_REGION="us-west-2"
CLUSTER_NAME="python-k8s-app-eks"
ACCOUNT_ID=$(aws sts get-caller-identity --query Account --output text)

echo -e "${GREEN}Setting up AWS EKS cluster: $CLUSTER_NAME${NC}"

# Check if AWS CLI is configured
if ! aws sts get-caller-identity &> /dev/null; then
    echo -e "${RED}AWS CLI is not configured. Please run 'aws configure' first.${NC}"
    exit 1
fi

# Check if required tools are installed
for tool in kubectl terraform docker; do
    if ! command -v $tool &> /dev/null; then
        echo -e "${RED}$tool is not installed. Please install it first.${NC}"
        exit 1
    fi
done

echo -e "${YELLOW}Installing eksctl...${NC}"
curl --silent --location "https://github.com/weaveworks/eksctl/releases/latest/download/eksctl_$(uname -s)_amd64.tar.gz" | tar xz -C /tmp
sudo mv /tmp/eksctl /usr/local/bin

echo -e "${YELLOW}Installing AWS Load Balancer Controller...${NC}"
# Install AWS Load Balancer Controller
kubectl apply -k "github.com/aws/eks-charts/stable/aws-load-balancer-controller/crds?ref=master"

# Create IAM policy for AWS Load Balancer Controller
cat > aws-load-balancer-controller-policy.json << EOF
